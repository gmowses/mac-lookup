// ── MAC Logic ─────────────────────────────────────────────────────────────────
function normalizeMac(raw: string): string | null {
  const clean = raw.replace(/[^0-9A-Fa-f]/g, '')
  if (clean.length !== 12) return null
  return clean.toUpperCase()
}

function formatMac(raw: string, sep: string): string {
  const groups = []
  for (let i = 0; i < 12; i += 2) groups.push(raw.slice(i, i + 2))
  return groups.join(sep)
}

function formatDot(raw: string): string {
  return `${raw.slice(0, 4)}.${raw.slice(4, 8)}.${raw.slice(8, 12)}`
}

function getOUI(mac: string): string {
  return mac.slice(0, 6).toUpperCase()
}

function getVendor(oui: string): string | null {
  return OUI_DB[oui] ?? null
}

function getMacType(mac: string): string {
  if (mac === 'FFFFFFFFFFFF') return 'broadcast'
  const firstByte = parseInt(mac.slice(0, 2), 16)
  if (firstByte & 0x01) return 'multicast'
  if (firstByte & 0x02) return 'localAdmin'
  return 'globalAdmin'
}

